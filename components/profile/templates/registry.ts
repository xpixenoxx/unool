export function getTemplateById(id: string): TemplateMeta | undefined {
  return TEMPLATE_REGISTRY.find(t => t.id === id);
}

export function getTemplatesByCategory(category: TemplateMeta['category']): TemplateMeta[] {
  return TEMPLATE_REGISTRY.filter(t => t.category === category);
}

export function getTemplatesByIntensity(intensity: TemplateMeta['intensity']): TemplateMeta[] {
  return TEMPLATE_REGISTRY.filter(t => t.intensity === intensity);
}

export function getDefaultTemplate(category: TemplateMeta['category'] = 'essential'): TemplateMeta {
  return TEMPLATE_REGISTRY.find(t => t.category === category && t.intensity === 'standard')!;
}