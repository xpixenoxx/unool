import type { TemplateMeta } from './types';

function createThumbnail(category: string, intensity: string): string {
  const colors: Record<string, string> = {
    essential: '0cc',
    professional: '08f',
    creative: 'f0c',
    technical: '0f0',
    social: '0cf',
  };
  const color = colors[category] || '0cc';
  return `data:image/svg+xml,${encodeURIComponent(`
    <svg xmlns="http://www.w3.org/2000/svg" width="300" height="200" viewBox="0 0 300 200">
      <rect width="300" height="200" fill="#${color}0d"/>
      <text x="150" y="100" font-family="system-ui" font-size="14" fill="#${color}" text-anchor="middle" dominant-baseline="middle">${category} • ${intensity}</text>
    </svg>
  `)}`;
}

